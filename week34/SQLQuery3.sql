SELECT CLB.MACLB, CLB.TENCLB, S.TENSAN, S.DIACHI, CT.COUNT(*) AS SOLUONGCTNN
FROM CAULACBO CLB
	JOIN SANVD S ON S.MASAN = CLB.MASAN
	JOIN CAUTHU CT ON CT.MACLB = CLB.MACLB
GROUP BY CLB.MACLB

