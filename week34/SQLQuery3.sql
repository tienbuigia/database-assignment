-- #6:
WITH BESTCLB AS
(
	SELECT TOP 1 MACLB, SUM(CAST(LEFT(HIEUSO, 1) AS int)) AS SOBANTHANG FROM BANGXH
	WHERE NAM = 2009
		AND VONG <= 3
	GROUP BY MACLB
	ORDER BY SOBANTHANG DESC
)
SELECT TENCLB, T.TENTINH
FROM CAULACBO CLB
	JOIN TINH T ON T.MATINH = CLB.MATINH
	JOIN BESTCLB BC ON BC.MACLB = CLB.MACLB;

